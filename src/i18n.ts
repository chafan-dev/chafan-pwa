// TODO remove this function.
export const translateErrorMsgCN = (s: string): string => {
  if (s in errorMsgCN) {
    return errorMsgCN[s];
  }
  if (s + '.' in errorMsgCN) {
    return errorMsgCN[s + '.'];
  }
  if (s[s.length - 1] === '.' && s.substring(0, s.length - 1) in errorMsgCN) {
    return errorMsgCN[s.substring(0, s.length - 1)];
  }
  return s;
};
